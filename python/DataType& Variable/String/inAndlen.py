
my_name="Hafizur Rahman"

print ("Hafi" in my_name)
print("Rahman" not in my_name)
print("Rony" in my_name)

print("Length of my_name:", len(my_name))
print("Type of my_name:", type(my_name))


#Indexing 
print("First character:", my_name[0])
print("Fifth character:", my_name[4])


#Nagative Indexing
print("Last character:", my_name[-1])
print("Second last character:", my_name[-2])

#String Scaling
#string [start : stop]
print("Substring from index 0 to 5:", my_name[0:5])
print("Substring from index 5 to 12:", my_name[5:12])